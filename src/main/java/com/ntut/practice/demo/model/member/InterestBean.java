package com.ntut.practice.demo.model.member;

public class InterestBean {
	Integer interestOid;
	String interestValue;
	String interestDesc;
	public Integer getInterestOid() {
		return interestOid;
	}
	public void setInterestOid(Integer interestOid) {
		this.interestOid = interestOid;
	}
	public String getInterestValue() {
		return interestValue;
	}
	public void setInterestValue(String interestValue) {
		this.interestValue = interestValue;
	}
	public String getInterestDesc() {
		return interestDesc;
	}
	public void setInterestDesc(String interestDesc) {
		this.interestDesc = interestDesc;
	}
	
}
