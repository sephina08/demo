package com.ntut.practice.demo.model.member;

public class ZoneBean {
	private Integer zoneOid;
	private Integer zoneParent;
	private String zoneName;
	public Integer getZoneOid() {
		return zoneOid;
	}
	public void setZoneOid(Integer zoneOid) {
		this.zoneOid = zoneOid;
	}
	
	public String getZoneName() {
		return zoneName;
	}
	public void setZoneName(String zoneName) {
		this.zoneName = zoneName;
	}
	public Integer getZoneParent() {
		return zoneParent;
	}
	public void setZoneParent(Integer zoneParent) {
		this.zoneParent = zoneParent;
	}

	
}
